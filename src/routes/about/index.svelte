    <span class="font-extrabold text-xl">What is GitHub and why use it?</span>
    <hr>
    <p class="pt-3">
        GitHub is a free online repository which allows you, the user, to store and access public or private data. GitHub is mostly popular within the Software Engineering community 
        but you can use it for mostly anything really! As you may know, servers cost money and data storage can become quite expensive, which is not a service that can reliably be offered by free applications.
        <br>
        That is why we use GitHub! As a well-established free service, GitHub allows you, the user, to store information. We, at Nalaccounting, merely interpret the data to store it on your
        personal account, in a dedicated private space (understand: repository)! It is great, really, because we do not store anything about you or your money, you retain ownership of the data,
        we do not spend a dime on hosting and everyone is happy. Until something breaks of course.
